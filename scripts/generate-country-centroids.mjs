// Generates src/data/country-centroids.ts (ISO-2 -> lat/lng) by parsing the SVG
// paths in src/data/world-map-paths.ts and inverting their equirectangular
// projection. Keeping centroids derived from the same source guarantees they
// stay aligned with the existing world map.
//
// Usage: node scripts/generate-country-centroids.mjs

import fs from "node:fs/promises";
import path from "node:path";

const WIDTH = 960;
const HEIGHT = 520;

const inputPath = path.join(process.cwd(), "src", "data", "world-map-paths.ts");
const outputPath = path.join(process.cwd(), "src", "data", "country-centroids.ts");
const dotsOutputPath = path.join(process.cwd(), "src", "data", "globe-land-dots.ts");

const src = await fs.readFile(inputPath, "utf8");

// The data file is `export const worldMapCountries: WorldMapCountryPath[] = [ ... ];`
const marker = "worldMapCountries: WorldMapCountryPath[] = ";
const markerIdx = src.indexOf(marker);
if (markerIdx < 0) throw new Error("Could not find worldMapCountries array in world-map-paths.ts");
// Start the search AFTER the marker so we skip the `[]` in `WorldMapCountryPath[]`.
const arrStart = src.indexOf("[", markerIdx + marker.length);
const arrEnd = src.lastIndexOf("]");
const countries = JSON.parse(src.slice(arrStart, arrEnd + 1));

/** Split an SVG path string into rings (arrays of [x, y] points). */
function ringsFromPath(d) {
  const rings = [];
  const subs = d.split("M").map((s) => s.trim()).filter(Boolean);
  for (const sub of subs) {
    const nums = sub
      .replace(/[ZL]/gi, " ")
      .trim()
      .split(/[\s,]+/)
      .map(Number)
      .filter((n) => !Number.isNaN(n));
    const pts = [];
    for (let i = 0; i + 1 < nums.length; i += 2) pts.push([nums[i], nums[i + 1]]);
    if (pts.length >= 3) rings.push(pts);
  }
  return rings;
}

/** Area-weighted centroid of a polygon ring (shoelace formula). */
function ringCentroid(pts) {
  let area = 0;
  let cx = 0;
  let cy = 0;
  for (let i = 0; i < pts.length; i++) {
    const [x0, y0] = pts[i];
    const [x1, y1] = pts[(i + 1) % pts.length];
    const cross = x0 * y1 - x1 * y0;
    area += cross;
    cx += (x0 + x1) * cross;
    cy += (y0 + y1) * cross;
  }
  area *= 0.5;
  if (Math.abs(area) < 1e-6) {
    const ax = pts.reduce((s, p) => s + p[0], 0) / pts.length;
    const ay = pts.reduce((s, p) => s + p[1], 0) / pts.length;
    return { area: 0, cx: ax, cy: ay };
  }
  return { area: Math.abs(area), cx: cx / (6 * area), cy: cy / (6 * area) };
}

/** Representative screen point: centroid of the country's largest landmass. */
function centroidScreen(d) {
  const rings = ringsFromPath(d);
  if (!rings.length) return null;
  let best = null;
  for (const ring of rings) {
    const c = ringCentroid(ring);
    if (!best || c.area > best.area) best = c;
  }
  return best ? [best.cx, best.cy] : null;
}

function toLonLat([x, y]) {
  const lon = (x / WIDTH) * 360 - 180;
  const lat = 90 - (y / HEIGHT) * 180;
  return [Number(lat.toFixed(2)), Number(lon.toFixed(2))];
}

const out = [];
for (const country of countries) {
  const screen = centroidScreen(country.path);
  if (!screen) continue;
  const [lat, lng] = toLonLat(screen);
  out.push({ code: country.code, name: country.name, lat, lng });
}

// Micro / island states are simplified out of the source SVG (their polygons
// collapse below the 1.8px threshold), yet several are XIPHIAS-served markets.
// Fill any that are missing with curated centroids. These never override the
// derived values above — they only add codes that the source map dropped.
const supplement = [
  { code: "AG", name: "Antigua and Barbuda", lat: 17.27, lng: -61.8 },
  { code: "CW", name: "Curaçao", lat: 12.17, lng: -68.99 },
  { code: "DM", name: "Dominica", lat: 15.41, lng: -61.37 },
  { code: "GD", name: "Grenada", lat: 12.12, lng: -61.68 },
  { code: "HK", name: "Hong Kong", lat: 22.32, lng: 114.17 },
  { code: "KN", name: "Saint Kitts and Nevis", lat: 17.3, lng: -62.73 },
  { code: "LC", name: "Saint Lucia", lat: 13.91, lng: -60.98 },
  { code: "MC", name: "Monaco", lat: 43.74, lng: 7.42 },
  { code: "MT", name: "Malta", lat: 35.94, lng: 14.38 },
  { code: "MU", name: "Mauritius", lat: -20.28, lng: 57.55 },
  { code: "NR", name: "Nauru", lat: -0.52, lng: 166.93 },
  { code: "SG", name: "Singapore", lat: 1.35, lng: 103.82 },
  { code: "ST", name: "São Tomé and Príncipe", lat: 0.19, lng: 6.61 },
  { code: "BH", name: "Bahrain", lat: 26.07, lng: 50.55 },
];
const haveCodes = new Set(out.map((entry) => entry.code.toUpperCase()));
for (const entry of supplement) {
  if (!haveCodes.has(entry.code.toUpperCase())) out.push(entry);
}

out.sort((a, b) => a.name.localeCompare(b.name));

const ts = `// AUTO-GENERATED by scripts/generate-country-centroids.mjs — do not edit by hand.
// Derived from src/data/world-map-paths.ts (equirectangular projection) so the
// centroids stay consistent with the existing 2D world map.

export type CountryCentroid = {
  /** ISO 3166-1 alpha-2 code */
  code: string;
  name: string;
  /** Latitude in degrees (north positive) */
  lat: number;
  /** Longitude in degrees (east positive) */
  lng: number;
};

export const countryCentroids: CountryCentroid[] = ${JSON.stringify(out)};

const byCode = new Map(countryCentroids.map((entry) => [entry.code.toUpperCase(), entry] as const));

/** Look up a centroid by ISO-2 code (case-insensitive). */
export function centroidForCode(code?: string | null): CountryCentroid | undefined {
  if (!code) return undefined;
  return byCode.get(code.toUpperCase());
}
`;

await fs.writeFile(outputPath, ts, "utf8");
console.log(`Wrote ${out.length} country centroids -> ${path.relative(process.cwd(), outputPath)}`);

// ---------------------------------------------------------------------------
// Land-dot point cloud: every country outline vertex, projected back to
// lat/lng and snapped to a coarse grid to dedupe. Rendered as a single
// THREE.Points cloud so the globe reads as a recognisable dotted Earth.
// ---------------------------------------------------------------------------
const GRID = 1.5; // degrees — snap resolution (smaller = denser dots)
const seen = new Set();
const dots = [];
for (const country of countries) {
  for (const ring of ringsFromPath(country.path)) {
    for (const point of ring) {
      const [lat, lng] = toLonLat(point);
      const key = `${Math.round(lat / GRID)}:${Math.round(lng / GRID)}`;
      if (seen.has(key)) continue;
      seen.add(key);
      dots.push([Number(lat.toFixed(2)), Number(lng.toFixed(2))]);
    }
  }
}

const dotsTs = `// AUTO-GENERATED by scripts/generate-country-centroids.mjs — do not edit by hand.
// A deduped land-outline point cloud (lat/lng pairs) sampled from
// src/data/world-map-paths.ts, used to render the globe's dotted surface.

/** [latitude, longitude] in degrees. */
export type LandDot = [number, number];

export const globeLandDots: LandDot[] = ${JSON.stringify(dots)};
`;

await fs.writeFile(dotsOutputPath, dotsTs, "utf8");
console.log(`Wrote ${dots.length} land dots -> ${path.relative(process.cwd(), dotsOutputPath)}`);
