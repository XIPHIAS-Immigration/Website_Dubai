export function getAllEvents(): EventRecord[] {
  return [...EVENTS].sort((a, b) => b.date.localeCompare(a.date));
}

export function getEventBySlug(slug: string): EventRecord | undefined {
  return EVENTS.find((event) => event.slug === slug);
}

export function groupEventsByYear(
  events: EventRecord[],
): Record<string, EventRecord[]> {
  return events.reduce<Record<string, EventRecord[]>>((acc, event) => {
    const year = event.date.slice(0, 4);
    if (!acc[year]) acc[year] = [];
    acc[year].push(event);
    return acc;
  }, {});
}
